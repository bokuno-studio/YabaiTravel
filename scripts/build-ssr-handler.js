/**
 * api/ssr.js を生成するビルドスクリプト
 *
 * 1. dist/index.html をHTMLテンプレートとして埋め込む
 * 2. dist/server/entry-server.js のSSRコードをインライン化
 *    → api/ssr.js 単体で SSR が完結（外部参照なし = Vercel serverless 対応）
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')

const templatePath = path.resolve(ROOT, 'dist/index.html')
if (!fs.existsSync(templatePath)) {
  console.error('Error: dist/index.html not found. Run vite build first.')
  process.exit(1)
}

const ssrBundlePath = path.resolve(ROOT, 'dist/server/entry-server.js')
if (!fs.existsSync(ssrBundlePath)) {
  console.error('Error: dist/server/entry-server.js not found. Run vite build --ssr first.')
  process.exit(1)
}

const template = fs.readFileSync(templatePath, 'utf-8')
const templateLiteral = JSON.stringify(template)

// Read SSR bundle
let ssrBundle = fs.readFileSync(ssrBundlePath, 'utf-8')

// Inline any asset imports from dist/server/assets/
const assetsDir = path.resolve(ROOT, 'dist/server/assets')
if (fs.existsSync(assetsDir)) {
  const assetFiles = fs.readdirSync(assetsDir).filter(f => f.endsWith('.js'))
  for (const assetFile of assetFiles) {
    const assetContent = fs.readFileSync(path.resolve(assetsDir, assetFile), 'utf-8')
    const escapedName = assetFile.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
    const importRegex = new RegExp(
      'import\\([\'"]\\./assets/' + escapedName + '[\'"]\\)',
      'g'
    )
    if (importRegex.test(ssrBundle)) {
      const wrapped = 'Promise.resolve((function() { ' + assetContent + '; return {}; })())'
      ssrBundle = ssrBundle.replace(importRegex, wrapped)
    }
  }
}

// Build the output
const parts = [
  '/**',
  ' * Vercel Serverless Function - SSR',
  ' * Auto-generated by scripts/build-ssr-handler.js. Do not edit.',
  ' * entry-server.js is inlined (no external dist/server/ reference).',
  ' */',
  '',
  '// --- Inlined SSR bundle ---',
  ssrBundle,
  '// --- End SSR bundle ---',
  '',
  'const TEMPLATE = ' + templateLiteral,
  '',
  'export default function handler(req, res) {',
  '  try {',
  '    const url = req.url || "/"',
  '    const { html: appHtml } = render(url)',
  '',
  '    let finalHtml = TEMPLATE',
  '    finalHtml = finalHtml.replace("<!--ssr-outlet-->", appHtml)',
  '',
  '    res.setHeader("Content-Type", "text/html; charset=utf-8")',
  '    res.setHeader("Cache-Control", "s-maxage=60, stale-while-revalidate=300")',
  '    res.status(200).end(finalHtml)',
  '  } catch (e) {',
  '    console.error("SSR Error:", e)',
  '    const fallback = TEMPLATE.replace("<!--ssr-outlet-->", "")',
  '    res.setHeader("Content-Type", "text/html; charset=utf-8")',
  '    res.status(200).end(fallback)',
  '  }',
  '}',
  '',
]

const ssrHandler = parts.join('\n')

const outputPath = path.resolve(ROOT, 'api/ssr.js')
fs.writeFileSync(outputPath, ssrHandler, 'utf-8')

const sizeKB = (Buffer.byteLength(ssrHandler, 'utf-8') / 1024).toFixed(1)
console.log('api/ssr.js generated (' + sizeKB + ' KB, SSR bundle inlined)')
