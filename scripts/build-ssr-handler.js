/**
 * api/ssr.js を生成するビルドスクリプト
 *
 * 1. dist/index.html をHTMLテンプレートとして埋め込む
 * 2. dist/server/entry-server.js のSSRコードをインライン化
 * 3. ハードコードされた環境変数をランタイム参照に置換（gitleaks 対策）
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')

// Load .env.local so VITE_* values are available for replacement
for (const envFile of ['.env', '.env.local']) {
  const envPath = path.resolve(ROOT, envFile)
  if (fs.existsSync(envPath)) {
    for (const line of fs.readFileSync(envPath, 'utf-8').split('\n')) {
      const trimmed = line.trim()
      if (!trimmed || trimmed.startsWith('#')) continue
      const eq = trimmed.indexOf('=')
      if (eq === -1) continue
      const key = trimmed.slice(0, eq)
      const val = trimmed.slice(eq + 1)
      if (!process.env[key]) process.env[key] = val
    }
  }
}

const templatePath = path.resolve(ROOT, 'dist/index.html')
if (!fs.existsSync(templatePath)) {
  console.error('Error: dist/index.html not found. Run vite build first.')
  process.exit(1)
}

const ssrBundlePath = path.resolve(ROOT, 'dist/server/entry-server.js')
if (!fs.existsSync(ssrBundlePath)) {
  console.error('Error: dist/server/entry-server.js not found. Run vite build --ssr first.')
  process.exit(1)
}

const template = fs.readFileSync(templatePath, 'utf-8')
const templateLiteral = JSON.stringify(template)

// Read SSR bundle
let ssrBundle = fs.readFileSync(ssrBundlePath, 'utf-8')

// Inline asset imports from dist/server/assets/
const assetsDir = path.resolve(ROOT, 'dist/server/assets')
if (fs.existsSync(assetsDir)) {
  const assetFiles = fs.readdirSync(assetsDir).filter(f => f.endsWith('.js'))
  for (const assetFile of assetFiles) {
    const assetContent = fs.readFileSync(path.resolve(assetsDir, assetFile), 'utf-8')
    const escapedName = assetFile.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
    const importRegex = new RegExp(
      'import\\([\'"]\\./assets/' + escapedName + '[\'"]\\)',
      'g'
    )
    if (importRegex.test(ssrBundle)) {
      const wrapped = 'Promise.resolve((function() { ' + assetContent + '; return {}; })())'
      ssrBundle = ssrBundle.replace(importRegex, wrapped)
    }
  }
}

// Replace hardcoded env values with runtime process.env references (gitleaks avoidance)
// Vite inlines VITE_* env vars as string literals during SSR build.
// Supabase needs valid fallbacks because createClient validates the URL at module init.
const envVarsToReplace = {
  'VITE_SUPABASE_URL': 'https://placeholder.supabase.co',
  'VITE_SUPABASE_ANON_KEY': 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.placeholder',
  'VITE_SUPABASE_SCHEMA': 'yabai_travel',
  'VITE_GOOGLE_MAPS_KEY': '',
  'VITE_GOOGLE_MAPS_API_KEY': '',
  'VITE_SENTRY_DSN': '',
  'VITE_ENABLE_COMMENTS': '',
  'VITE_RAKUTEN_APP_ID': '',
  'VITE_RAKUTEN_AFFILIATE_ID': '',
}

for (const [envVar, fallback] of Object.entries(envVarsToReplace)) {
  const value = process.env[envVar]
  if (value) {
    const escaped = value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
    const regex = new RegExp('"' + escaped + '"', 'g')
    const fallbackEscaped = JSON.stringify(fallback)
    const replacement = '(process.env.' + envVar + ' || ' + fallbackEscaped + ')'
    const count = (ssrBundle.match(regex) || []).length
    if (count > 0) {
      ssrBundle = ssrBundle.replace(regex, replacement)
      console.log('  Replaced ' + envVar + ' (' + count + ' occurrences)')
    }
  }
}

// Build output
const parts = [
  '/**',
  ' * Vercel Serverless Function - SSR',
  ' * Auto-generated by scripts/build-ssr-handler.js. Do not edit.',
  ' * entry-server.js is inlined (no external dist/server/ reference).',
  ' */',
  '',
  '// --- Inlined SSR bundle ---',
  ssrBundle,
  '// --- End SSR bundle ---',
  '',
  'const TEMPLATE = ' + templateLiteral,
  '',
  '// Prefetch events from Supabase for top page SSR data injection',
  '// Uses AbortController timeout to avoid blocking SSR if Supabase is slow',
  'async function prefetchEvents() {',
  '  const url = process.env.VITE_SUPABASE_URL',
  '  const key = process.env.VITE_SUPABASE_ANON_KEY',
  '  const schema = process.env.VITE_SUPABASE_SCHEMA || "yabai_travel"',
  '  if (!url || !key) return null',
  '  try {',
  '    const today = new Date().toISOString().slice(0, 10)',
  '    const controller = new AbortController()',
  '    const timeout = setTimeout(() => controller.abort(), 3000)',
  '    const res = await fetch(',
  '      `${url}/rest/v1/events?select=*,categories(*)&event_date=gte.${today}&order=event_date.asc&limit=500`,',
  '      {',
  '        headers: {',
  '          apikey: key,',
  '          Authorization: `Bearer ${key}`,',
  '          "Accept-Profile": schema,',
  '        },',
  '        signal: controller.signal,',
  '      }',
  '    )',
  '    clearTimeout(timeout)',
  '    if (!res.ok) return null',
  '    return await res.json()',
  '  } catch (e) {',
  '    if (e.name !== "AbortError") console.error("SSR prefetch error:", e)',
  '    return null',
  '  }',
  '}',
  '',
  'export default async function handler(req, res) {',
  '  const originalUrl = req.headers["x-invoke-path"] || req.headers["x-matched-path"] || req.url || "/"',
  '  const url = originalUrl.startsWith("/api/ssr") ? originalUrl.replace(/^\\/api\\/ssr/, "") || "/" : originalUrl',
  '  try {',
  '    const { html: appHtml } = render(url)',
  '',
  '    const lang = url.startsWith("/en") ? "en" : "ja"',
  '    let finalHtml = TEMPLATE',
  '    finalHtml = finalHtml.replace(/lang="[^"]*"/, \'lang="\' + lang + \'"\')',
  '',
  '    // Extract title and description from appHtml and inject into TEMPLATE',
  '    const titleMatch = appHtml.match(/<title>([^<]*)<\\/title>/)',
  '    if (titleMatch) {',
  '      finalHtml = finalHtml.replace(/<title>[^<]*<\\/title>/, "<title>" + titleMatch[1] + "<\\/title>")',
  '      finalHtml = finalHtml.replace(/(<meta property="og:title" content=")[^"]*("/), \'$1\' + titleMatch[1] + \'$2\')',
  '    }',
  '    const descMatch = appHtml.match(/<meta name="description" content="([^"]*)"/)',
  '    if (descMatch) {',
  '      finalHtml = finalHtml.replace(/(<meta name="description" content=")[^"]*("/), \'$1\' + descMatch[1] + \'$2\')',
  '      finalHtml = finalHtml.replace(/(<meta property="og:description" content=")[^"]*("/), \'$1\' + descMatch[1] + \'$2\')',
  '    }',
  '',
  '    finalHtml = finalHtml.replace("<!--ssr-outlet-->", appHtml)',
  '',
  '    // Prefetch events for top page to eliminate client-side fetch latency',
  '    const isTopPage = /^\\/(ja|en)\\/?$/.test(url) || /^\\/(ja|en)\\?/.test(url)',
  '    if (isTopPage) {',
  '      const events = await prefetchEvents()',
  '      if (events) {',
  '        const jsonStr = JSON.stringify(events).split("<").join("\\\\u003c")',
  '        const dataScript = "<script>window.__SSR_EVENTS__=" + jsonStr + "</script>"',
  '        finalHtml = finalHtml.replace("</head>", dataScript + "</head>")',
  '      }',
  '    }',
  '',
  '    res.setHeader("Content-Type", "text/html; charset=utf-8")',
  '    res.setHeader("Cache-Control", "s-maxage=60, stale-while-revalidate=300")',
  '    res.status(200).end(finalHtml)',
  '  } catch (e) {',
  '    console.error("SSR Error:", e)',
  '    const fallbackLang = url.startsWith("/en") ? "en" : "ja"',
  '    const fallback = TEMPLATE.replace(/lang="[^"]*"/, \'lang="\' + fallbackLang + \'"\').replace("<!--ssr-outlet-->", "")',
  '    res.setHeader("Content-Type", "text/html; charset=utf-8")',
  '    res.status(200).end(fallback)',
  '  }',
  '}',
  '',
]

const ssrHandler = parts.join('\n')

const outputPath = path.resolve(ROOT, 'api/ssr.js')
fs.writeFileSync(outputPath, ssrHandler, 'utf-8')

const sizeKB = (Buffer.byteLength(ssrHandler, 'utf-8') / 1024).toFixed(1)
console.log('api/ssr.js generated (' + sizeKB + ' KB, SSR bundle inlined, env vars replaced)')

// Remove index.html from all output locations so Vercel doesn't serve it as a static file
// (all requests should go through api/ssr for SSR)
const indexPaths = [
  path.resolve(ROOT, 'dist/index.html'),
  path.resolve(ROOT, '.vercel/output/static/index.html'),
]
for (const p of indexPaths) {
  if (fs.existsSync(p)) {
    fs.unlinkSync(p)
    console.log(path.relative(ROOT, p) + ' removed (SSR handles all HTML requests)')
  }
}
